public class Q1 {
    /*
     * Given two positive integers X and Y. You have to add two integers and output
     * their sum.
     * 
     * 
     * 
     * Example 1:
     * 
     * Input:
     * X = 3, Y = 4
     * Output:
     * 7
     * Explanation:
     * Sum of X and Y is 7
     */
    class MathematicalOperation {

        static BigInteger add(BigInteger x, BigInteger y) {

            return x.add(y);

        }

    }
}
